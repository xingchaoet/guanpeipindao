function tsfl_more_close(e){
if (e.innerHTML=='更多'){
document.getElementById("div_tsfl2").style.display="";
document.getElementById("div_tsfl1").style.display="none";
e.innerHTML='收起';
}
else
{
document.getElementById("div_tsfl1").style.display="";
document.getElementById("div_tsfl2").style.display="none";
e.innerHTML='更多';
}
}

